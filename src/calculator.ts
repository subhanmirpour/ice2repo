let add = (a: number, b: number) => {
    return a + b;
};
let subtract = (a: number, b: number) => {
    return a - b;
};
let multiply = (a: number, b: number) => {
    return a * b;
};
let divide = (a: number, b: number) => {
    return a / b;
};
module.exports = {
    add: add,
    subtract: subtract,
    multiply: multiply,
    divide: divide
};
